public class NumerosPares {

    public static void main(String[] args) {


        for(int i=0;i<=10;i++){
            System.out.print(" " + i);

        }

        System.out.println("Agora utilizando o while: ");

        int numero = 0;

        while (numero <= 10){
            System.out.print(numero + " ");
            numero ++;
        }






    }
}
